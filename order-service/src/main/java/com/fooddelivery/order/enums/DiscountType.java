/**
 * Type of discount for promo codes
 */
public enum DiscountType {
    /**
     * Percentage discount (e.g., 10% off)
     */
    PERCENTAGE,

    /**
     * Fixed amount discount (e.g., $5 off)
     */
    FIXED_AMOUNT,

    /**
     * Free delivery
     */
    FREE_DELIVERY
}

