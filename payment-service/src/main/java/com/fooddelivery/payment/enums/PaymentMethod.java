package com.fooddelivery.payment.enums;

/**
 * Payment method enum
 */
public enum PaymentMethod {
    /**
     * Credit or debit card payment
     */
    CARD,
    
    /**
     * Cash on delivery
     */
    CASH,
    
    /**
     * Digital wallet (e.g., PayPal, Apple Pay, Google Pay)
     */
    WALLET
}

